/**
 * SDK Conversion / Round-Trip Tests
 *
 * Proves the critical migration path: a standard markdown-based squad
 * (.squad/ with team.md, routing.md, agent charters) can be "converted"
 * to SDK mode (squad.config.ts using builder functions), and `squad build`
 * regenerates functionally-equivalent .squad/ markdown from the typed config.
 *
 * Test coverage:
 *   1. Original markdown squad structure is valid and complete
 *   2. SDK config (via builders) models the same team
 *   3. Build output preserves team members, routing rules, agent identities
 *   4. Protected files (decisions.md, history.md, orchestration-log/) survive build
 *   5. Edge cases: custom content, extra agents, minimal squads
 *
 * ⚠️ Uses local stubs matching build-command.test.ts patterns.
 * When Fenster's build command lands, replace stubs with real imports.
 *
 * @see packages/squad-cli/src/cli/commands/build.ts
 * @see packages/squad-sdk/src/builders/index.ts
 * @module test/sdk-conversion
 */

