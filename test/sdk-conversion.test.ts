/**
 * Build the SDK config that models the same team as the hand-written
 * markdown squad above. This is what a user would write in squad.config.ts
 * when converting from markdown to SDK mode.
 */
function buildEquivalentSDKConfig(): SquadSDKConfig {
  return {
    version: '1.0.0',
    team: defineTeam({
      name: 'Alpha Squad',
      description: 'The core engineering team for the replatform.',
      members: ['edie', 'hockney'],
    }),
    agents: [
      defineAgent({
        name: 'edie',
        role: 'TypeScript Engineer',
        charter: 'Precise, opinionated. Types are contracts.',
        model: 'claude-sonnet-4',
      }),
      defineAgent({
        name: 'hockney',
        role: 'Tester',
        charter: 'Skeptical, relentless. If it can break, he\'ll find how.',
      }),
    ],
    routing: defineRouting({
      rules: [
        { pattern: 'feature-*', agents: ['edie'] },
        { pattern: 'test-*', agents: ['hockney'] },
        { pattern: 'bug-*', agents: ['edie', 'hockney'] },
      ],
      defaultAgent: 'edie',
    }),
    ceremonies: [
      defineCeremony({
        name: 'standup',
        schedule: '0 9 * * 1-5',
        participants: ['edie', 'hockney'],
      }),
    ],
  };
}

