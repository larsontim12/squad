function generateTeamMd(config: SquadSDKConfig): string {
  const lines: string[] = [GENERATED_HEADER, '', `# ${config.team.name}`];
  if (config.team.description) lines.push('', config.team.description);
  lines.push('', '## Members', '');
  for (const m of config.team.members) {
    const agent = config.agents.find((a) => a.name === m);
    lines.push(`- **${m}** — ${agent?.role ?? 'Unknown'}`);
  }
  return lines.join('\n') + '\n';
}

function generateRoutingMd(config: SquadSDKConfig): string {
  if (!config.routing) return '';
  const lines: string[] = [GENERATED_HEADER, '', '# Routing'];
  if (config.routing.defaultAgent) {
    lines.push('', `**Default agent:** ${config.routing.defaultAgent}`);
  }
  lines.push('', '## Rules', '');
  for (const rule of config.routing.rules) {
    lines.push(`- \`${rule.pattern}\` → ${rule.agents.join(', ')}`);
  }
  return lines.join('\n') + '\n';
}

function generateCharterMd(agent: AgentDefinition): string {
  const lines: string[] = [
    GENERATED_HEADER,
    '',
    `# ${agent.name} — ${agent.role}`,
  ];
  if (agent.charter) lines.push('', agent.charter);
  if (agent.model) lines.push('', `**Model:** ${agent.model}`);
  if (agent.tools && agent.tools.length > 0) {
    lines.push('', '## Tools', '');
    for (const t of agent.tools) lines.push(`- ${t}`);
  }
  return lines.join('\n') + '\n';
}

function generateCeremoniesMd(config: SquadSDKConfig): string {
  if (!config.ceremonies || config.ceremonies.length === 0) return '';
  const lines: string[] = [GENERATED_HEADER, '', '# Ceremonies'];
  for (const c of config.ceremonies) {
    lines.push('', `## ${c.name}`);
    if (c.schedule) lines.push(`**Schedule:** ${c.schedule}`);
    if (c.participants && c.participants.length > 0) {
      lines.push(`**Participants:** ${c.participants.join(', ')}`);
    }
    if (c.agenda) lines.push('', c.agenda);
  }
  return lines.join('\n') + '\n';
}

function runBuild(config: SquadSDKConfig, squadDir: string): { filesWritten: string[] } {
  const written: string[] = [];

  writeFileSync(join(squadDir, 'team.md'), generateTeamMd(config));
  written.push('team.md');

  if (config.routing) {
    writeFileSync(join(squadDir, 'routing.md'), generateRoutingMd(config));
    written.push('routing.md');
  }

  for (const agent of config.agents) {
    const charterDir = join(squadDir, 'agents', agent.name);
    mkdirSync(charterDir, { recursive: true });
    writeFileSync(join(charterDir, 'charter.md'), generateCharterMd(agent));
    written.push(`agents/${agent.name}/charter.md`);
  }

  if (config.ceremonies && config.ceremonies.length > 0) {
    writeFileSync(join(squadDir, 'ceremonies.md'), generateCeremoniesMd(config));
    written.push('ceremonies.md');
  }

  return { filesWritten: written };
}

