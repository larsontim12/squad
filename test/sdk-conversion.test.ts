function defineTeam(config: TeamDefinition): TeamDefinition {
  if (!config.name || config.name.trim() === '') throw new Error('defineTeam: name required');
  if (!Array.isArray(config.members) || config.members.length === 0) throw new Error('defineTeam: members required');
  return config;
}

function defineAgent(config: AgentDefinition): AgentDefinition {
  if (!config.name || config.name.trim() === '') throw new Error('defineAgent: name required');
  if (!config.role || config.role.trim() === '') throw new Error('defineAgent: role required');
  return config;
}

function defineRouting(config: RoutingDefinition): RoutingDefinition {
  if (!Array.isArray(config.rules)) throw new Error('defineRouting: rules must be an array');
  return config;
}

function defineCeremony(config: CeremonyDefinition): CeremonyDefinition {
  if (!config.name || config.name.trim() === '') throw new Error('defineCeremony: name required');
  return config;
}

